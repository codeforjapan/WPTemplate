      <h2 class="headings">Code for Japan Brigadeとは？</h2>
      <div class="secbox">
        <div class="secbox__left">
          <p>Code for Japanが提供する連携プログラムに参加している各地のコミュニティを、Code for Japan Brigade（ブリゲード）と呼んでいます。</p>
          <p>各Brigadeは活動地域の市民や自治体と連携し、テクノロジーを活用することで地域課題を解決します。</p>
        </div>
        <div class="secbox__right">
          <p>
※Code for Japan Brigade は、Code for Japan の下部組織ではありません。それぞれは独立した団体であり、Code for Japan が各地のコミュニティに指示を出すような関係ではありません。各地の課題を主体的に解決するコミュニティが Brigade であり、Code for Japan はその活動を連携し、ネットワークとして情報交換の機会を作るハブ的立場を取っています。
          </p>
        </div>
      </div>

